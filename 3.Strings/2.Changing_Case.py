name = "ada lovelace"

print(f"The original name of the person is {name}") #Print the name just as it is.
print(f"The new name using title casing is {name.title()}") #Print the name in title cases
print(f"Name in capitalise is {name.upper()}") #Print the name in Uppercase
print(name.isupper()) #Is the name Upper?
print(f"Name in capitalise is {name.lower()}") #Print the name in Uppercase